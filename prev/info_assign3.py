def send_alert_email(sender_email, password, recipient_email, df, target):

    high_df = df[df["severity"].isin(["High", "Critical"])]

    # ❌ Do NOT send if no high/critical
    if high_df.empty:
        return "No alert triggered"

    scan_time = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    overall_risk = int(df["risk_score"].sum())

    # -------------------------------
    # BUILD HTML EMAIL
    # -------------------------------
    html = f"""
    <h2 style="color:red;">🚨 CyberScan Security Alert</h2>

    <p><b>Target:</b> {target}</p>
    <p><b>Scan Time:</b> {scan_time}</p>
    <p><b>Overall Risk Score:</b> {overall_risk}</p>

    <h3>High & Critical Vulnerabilities</h3>

    <table border="1" cellpadding="6" cellspacing="0">
        <tr style="background-color:#f2f2f2;">
            <th>Vulnerability</th>
            <th>Severity</th>
            <th>Risk Score</th>
        </tr>
    """

    # -------------------------------
    # TABLE DATA
    # -------------------------------
    for _, row in high_df.iterrows():
        html += f"""
        <tr>
            <td>{row['service']}</td>
            <td>{row['severity']}</td>
            <td>{row['risk_score']}</td>
        </tr>
        """

    html += "</table><br>"

    # -------------------------------
    # RECOMMENDATIONS
    # -------------------------------
    html += "<h3>Recommended Actions</h3><ul>"

    for _, row in high_df.iterrows():

        if "SQL" in row["service"]:
            rec = "Sanitize inputs and use prepared statements"
        elif "Admin" in row["service"]:
            rec = "Restrict admin panel access"
        elif "HTTPS" in row["service"]:
            rec = "Enable HTTPS encryption"
        else:
            rec = "Review and secure this service"

        html += f"<li><b>{row['service']}:</b> {rec}</li>"

    html += "</ul>"

    # -------------------------------
    # DISCLAIMER
    # -------------------------------
    html += """
    <hr>
    <p style="color:gray; font-size:12px;">
    This is an automated alert generated by CyberScan.
    Please verify findings before taking action.
    </p>
    """

    # -------------------------------
    # EMAIL SETUP
    # -------------------------------
    msg = MIMEMultipart("alternative")
    msg["Subject"] = f"🚨 {target} - HIGH/CRITICAL Vulnerabilities Detected"
    msg["From"] = sender_email
    msg["To"] = recipient_email

    msg.attach(MIMEText(html, "html"))

    try:
        server = smtplib.SMTP("smtp.gmail.com", 587)
        server.starttls()
        server.login(sender_email, password)
        server.send_message(msg)
        server.quit()
        return "Email Sent Successfully"

    except Exception as e:
        return str(e)

