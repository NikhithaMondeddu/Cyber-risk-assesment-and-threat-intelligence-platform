import requests

def scan_vulnerabilities(target):

    results = []

    try:
        url = f"http://{target}"

        response = requests.get(url, timeout=5)
        headers = response.headers

        # 1. Missing Security Headers
        if "X-Frame-Options" not in headers:
            results.append(("Missing X-Frame-Options", "Medium", 5))

        if "Content-Security-Policy" not in headers:
            results.append(("Missing CSP Header", "High", 8))

        # 2. Server Disclosure
        if "Server" in headers:
            results.append(("Server Version Disclosure", "Low", 3))

        # 3. HTTP instead of HTTPS
        if url.startswith("http://"):
            results.append(("Not using HTTPS", "High", 9))

        # 4. Open Admin Panel
        admin_paths = ["/admin", "/login", "/dashboard"]

        for path in admin_paths:
            try:
                r = requests.get(url + path, timeout=3)
                if r.status_code == 200:
                    results.append(("Open Admin Panel", "Critical", 12))
            except:
                pass

        # 5. SQL Injection Test (basic)
        try:
            sqli = requests.get(url + "?id=1' OR '1'='1", timeout=3)
            if "sql" in sqli.text.lower():
                results.append(("Possible SQL Injection", "Critical", 13))
        except:
            pass

    except:
        results.append(("Target Unreachable", "Informational", 1))

    return results

